   const currentDatabase = databaseName[themeId];
   const firstNames = currentDatabase.firstName;
   const lastNames = currentDatabase.lastName;

   const getFirstName = firstNames[getRandomInt(firstNames.length)];
   const getLastName = lastNames[getRandomInt(lastNames.length)];

